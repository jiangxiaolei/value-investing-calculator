反过来，如果薪酬完全不与业绩挂钩，管理者和员工就没有动机去创造超额业绩。他们只需要保证在职就行。

**一个合理的设计应该是什么样的？**

以巴菲特的伯克希尔为参照：
- 每个子公司的CEO薪酬与**业务表现**挂钩，而非伯克希尔股价
- 奖金基于**ROE、经营利润、资本配置效率**等可量化的运营指标
- 薪酬结构强调长期，而非短期

